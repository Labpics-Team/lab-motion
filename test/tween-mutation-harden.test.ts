/**
 * test/tween-mutation-harden.test.ts — S43: закалка mutation-покрытия tween.ts.
 *
 * Baseline Stryker: 75.00% (ниже break=76, 5 выживших). tween — чистая линейная
 * интерполяция `from + (to−from)·t` с клампом эндпоинтов и overflow-фоллбеком.
 *
 * Закрываемые КЛАССЫ (прямые оракулы):
 *   T1 клампинг снизу (28:7 if t<=0): t<0 → from, НЕ обратная экстраполяция.
 *   T2 клампинг сверху (29:7 if t>=1): t>1 → to, НЕ прямая экстраполяция.
 *
 * Эквиваленты (28/29 `<=`↔`<`/`>=`↔`>`; 31 `if(true)`) — в блоке внизу с обоснованием.
 */

import { describe, expect, it } from 'vitest';
import { tween } from '../src/index.js';

// ─── T1 — клампинг снизу: t<0 → from (строка 28:7) ──────────────────────────────

describe('T1 клампинг снизу: t<0 → from, не экстраполяция назад (строка 28:7)', () => {
  it('tween(0,10,-0.5) === 0 (from, не −5)', () => {
    // Мутант 28:7 `if(false)`: пропуск клампа → raw = 0+(10−0)·(−0.5) = −5 (экстраполяция).
    // Здоровый клампит t≤0 → from. Оракул на from кусается.
    expect(tween(0, 10, -0.5)).toBe(0);
  });
  it('tween(5,20,-1) === 5; tween(100,0,-3) === 100', () => {
    expect(tween(5, 20, -1)).toBe(5);
    expect(tween(100, 0, -3)).toBe(100);
  });
});

// ─── T2 — клампинг сверху: t>1 → to (строка 29:7) ───────────────────────────────

describe('T2 клампинг сверху: t>1 → to, не экстраполяция вперёд (строка 29:7)', () => {
  it('tween(0,10,1.5) === 10 (to, не 15)', () => {
    // Мутант 29:7 `if(false)`: raw = 0+10·1.5 = 15 (экстраполяция). Здоровый клампит → to.
    expect(tween(0, 10, 1.5)).toBe(10);
  });
  it('tween(5,20,2) === 20; tween(100,0,3) === 0', () => {
    expect(tween(5, 20, 2)).toBe(20);
    expect(tween(100, 0, 3)).toBe(0);
  });
});

// ─── Общая корректность + overflow-фоллбек ──────────────────────────────────────

describe('tween общая корректность', () => {
  it('интерьер: tween(0,10,0.5)=5, эндпоинты точны (t=0→from, t=1→to)', () => {
    expect(tween(0, 10, 0.5)).toBe(5);
    expect(tween(3, 7, 0)).toBe(3); // t=0 → from точно
    expect(tween(3, 7, 1)).toBe(7); // t=1 → to точно
  });
  it('overflow: |from|+|to|>MAX_VALUE → two-point фоллбек, конечно (строки 31-33)', () => {
    // (to−from) = −1e308−1e308 = −2e308 → −Inf; raw = 1e308 + (−Inf)·0.5 = −Inf → не finite →
    // фоллбек from·(1−t)+to·t = 5e307−5e307 = 0. (При t→0 raw был бы NaN: 0·∞; здесь −Inf.)
    const r = tween(1e308, -1e308, 0.5);
    expect(Number.isFinite(r)).toBe(true);
    expect(r).toBeCloseTo(0, 0);
  });
});

// ─── Документированные ЭКВИВАЛЕНТНЫЕ мутанты ─────────────────────────────────────
//
// Не гоняются (Goodhart):
//   • 28:7 (`t <= 0` → `t < 0`) / 29:7 (`t >= 1` → `t > 1`): при t РОВНО 0/1 ранний
//     возврат пропускается, но raw САМ вычисляется в эндпоинт: t=0 → from+(to−from)·0
//     = from (для конечных; при overflow (to−from)·0=NaN → фоллбек from·1+to·0=from);
//     t=1 → from+(to−from)·1 = to. Тот же результат → эквивалент.
//   • 31:7 (`if(!isFinite(raw))` → `if(true)`): всегда two-point форма
//     from·(1−t)+to·t. Она МАТЕМАТИЧЕСКИ тождественна raw (обе — корректная линейная
//     интерполяция), отличие лишь в последних битах float для отдельных входов. Оба
//     возвращают конечное корректное значение → поведенчески эквивалент; убийство
//     требовало бы хрупкого bit-exact snapshot одного конкретного входа (Goodhart).
describe('документированные эквиваленты tween (обоснование, не театр)', () => {
  it('эндпоинты точны через ОБА пути (обоснование 28/29 `<=`↔`<`): t=0→from, t=1→to', () => {
    // Характеризация: raw в эндпоинте совпадает с ранним возвратом.
    expect(tween(2.5, 8.5, 0)).toBe(2.5);
    expect(tween(2.5, 8.5, 1)).toBe(8.5);
  });
  it('two-point форма даёт то же в интерьере (обоснование 31): корректная интерполяция', () => {
    // Характеризация: обе формы — линейная интерполяция; результат в [from,to].
    for (const t of [0.25, 0.5, 0.75]) {
      const r = tween(0, 100, t);
      expect(r).toBeCloseTo(100 * t, 6);
    }
  });
});
